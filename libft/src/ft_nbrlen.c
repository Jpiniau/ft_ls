#include "libft.h"

size_t	ft_nbrlen(int nbr)
{
	int	size;

	size = 0;
	if (nbr == 0)
		return (1);
	while (nbr > 0)
	{
		size++;
		nbr /= 10;
	}
	return (size);
}
